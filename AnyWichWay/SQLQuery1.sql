Select TOP 200 * FROM dbo.Sandwiches
WHERE	
	Salty > Sweet AND
	Salty >= Salty AND
	Salty > Sour AND
	Salty > Bitter AND
	Salty > Hot AND
	Encounter > Egg AND
	Encounter > Catching AND
	Encounter > [Exp] AND
	Encounter > Raid AND
	Encounter > ItemDrop AND
	Encounter > Humungo AND
	Encounter > Teensy AND
	Encounter >= Encounter AND
	Encounter > Title AND
	Encounter > Sparkling AND
	Normal >= Normal AND
	Normal > Fighting AND
	Normal > Flying AND
	Normal > Poison AND
	Normal > Ground AND
	Normal > Rock AND
	Normal > Bug AND
	Normal > Ghost AND
	Normal > Steel AND
	Normal > Fire AND
	Normal > Water AND
	Normal > Grass AND
	Normal > Electric AND
	Normal > Psychic AND
	Normal > Ice AND
	Normal > Dragon AND
	Normal > Dark AND
	Normal > Fairy AND
	Normal + Encounter > 0
ORDER BY Cost ASC